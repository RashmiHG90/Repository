let dd= new Date();
console.log("Current date and time: " + dd.toString());
console.log(dd.getDate()); //get date
console.log(dd.getMonth()); //get month, month index starts from 0
console.log(dd.getMonth()+1); //get month, month index starts from 0
console.log(dd.getFullYear());  //get year
console.log(dd.getHours()); //get hours
console.log(dd.getMinutes()); //get minutes
console.log(dd.getSeconds()); //get seconds
console.log(dd.getMilliseconds()); //get milliseconds
console.log(dd.getDay()); //get day index, Sunday=0, Monday=1       